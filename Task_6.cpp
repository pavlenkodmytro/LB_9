#include <iostream>    // Підключення бібліотеки для вводу/виводу
#include <ctime>       // Підключення бібліотеки для роботи з часом
#include <cstdlib>     // Підключення бібліотеки для генерації випадкових чисел

int main() {
    // Визначення розміру масиву
    const int size = 10; 

    // Ініціалізація масиву випадковими значеннями
    int arr[size]; // Оголошення масиву
    srand(time(0)); // Ініціалізація генератора випадкових чисел
    for (int i = 0; i < size; i++) {
        arr[i] = rand() % 10; // Генерація випадкових значень від 0 до 9
    }

    // Виведення масиву
    std::cout << "Array: "; 
    for (int i = 0; i < size; i++) {
        std::cout << arr[i] << " "; // Виведення кожного елемента масиву
    }
    std::cout << std::endl; // Перехід на новий рядок

    // Пошук першого нульового елемента в масиві
    bool foundZero = false; // Прапорець для перевірки, чи знайдено нуль
    int zeroIndex = -1; // Індекс нульового елемента (ініціалізація значенням -1)
    
    // Цикл для знаходження першого нульового елемента
    for (int i = 0; i < size; i++) {
        if (arr[i] == 0) { // Якщо знайдено нуль
            foundZero = true; // Встановлюємо прапорець
            zeroIndex = i; // Зберігаємо індекс
            break; // Виходимо з циклу, оскільки перший нуль знайдено
        }
    }

    // Виведення результату
    if (foundZero) {
        std::cout << "The first zero element is at index " << zeroIndex << std::endl; // Виведення індексу
    } else {
        std::cout << "The array does not contain zero elements." << std::endl; // Якщо нулів немає
    }

    return 0; // Завершення програми
}
