#include <iostream>    // Підключення бібліотеки для вводу/виводу
#include <ctime>       // Підключення бібліотеки для роботи з часом
#include <cstdlib>     // Підключення бібліотеки для генерації випадкових чисел

int main() {
    srand(time(NULL)); // Ініціалізація генератора випадкових чисел з поточним часом
    const int size = 10; // Визначення розміру масиву
    int arr[size]; // Оголошення масиву

    // Заповнення масиву випадковими числами від 0 до 99
    for (int i = 0; i < size; i++) {
        arr[i] = rand() % 100; // Генерація випадкових чисел
    }

    int max = arr[0]; // Ініціалізація максимального значення
    int secondMax = arr[0]; // Ініціалізація другого максимального значення

    // Цикл для знаходження максимального і другого максимального значень
    for (int i = 1; i < size; i++) {
        if (arr[i] > max) { // Якщо знайдено нове максимальне значення
            secondMax = max; // Оновлення другого максимального значення
            max = arr[i]; // Оновлення максимального значення
        } else if (arr[i] > secondMax && arr[i] != max) { // Якщо елемент більший за друге максимальне
            secondMax = arr[i]; // Оновлення другого максимального значення
        }
    }

    // Виведення другого найбільшого елемента
    std::cout << "Second largest element: " << secondMax << std::endl;

    return 0; // Завершення програми
}
